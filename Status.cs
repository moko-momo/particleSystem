public class Status {
	public float radius = 0f, angle = 0f, time = 0f, radiusChange = 0.02f; 
    public Status(float radius, float angle, float time, float radiusChange)  
    {  
        this.radius = radius;   
        this.angle = angle;     
		this.time = time;       
        radiusChange = this.radiusChange;   
    }  
}
